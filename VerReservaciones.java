    public void Cargardatos(){
       pReservacion cargar = new pReservacion();
      List<Reservacion> lstVerReservaciones = new ArrayList<Reservacion>();
      lstVerReservaciones = cargar.Reservacion();
      
      DefaultTableModel dft=(DefaultTableModel) tblVerReservacion.getModel();
      
      dft.setRowCount(0);
      for(Reservacion r:lstVerReservaciones){
          dft.addRow(new Object[]{IniciodelUsuario.r.getNombre(), r.getFechadeReserva(), r.getTipodeHabitacion(), r.getPrecio()});
          
      }
      
  }


